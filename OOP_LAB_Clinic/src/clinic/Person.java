package clinic;

public class Person {

	private String First;
	private String Last;
	private String SSN;
	
	public Person(String first, String last, String ssn) {
		this.First = first;
		this.Last = last;
		this.SSN = ssn;
	}
	
	public String getFirst() {
		return this.First;
	}

	public String getLast() {
		return this.Last;
	}

	public String getSSN() {
		return this.SSN;
	}
}
